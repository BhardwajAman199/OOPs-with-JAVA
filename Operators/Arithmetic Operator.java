class Main {
    public static void main(String[] args) {
    	double n1 = 19.5, n2 = 2.5, result;
    	result = n1 + n2;      	//  addition operator
    	System.out.println("number1 + number2 = " + result);
    	result = n1 - n2;   	// subtraction operator
    	System.out.println("number1 - number2 = " + result);
    	result = n1 * n2;      	// multiplication operator
    	System.out.println("number1 * number2 = " + result);
    	result = n1 / n2;        // division operator
    	System.out.println("number1 / number2 = " + result);
    	result = n1 % n2;     	// remainder operator
    	System.out.println("number1 % number2 = " + result);
    }
}
