class Main {

    public static void main(String[] args){
        int v1 = 1;
        int v2 = 2;
        if(v1 == v2)
            System.out.println("v1 == v2");
        if(v1 != v2)
            System.out.println("v1 != v2");
        if(v1 > v2)
            System.out.println("v1 > v2");
        if(v1 < v2)
            System.out.println("v1 < v2");
        if(v1 <= v2)
            System.out.println("v1 <= v2");
         if((v1 == 1) && (v2 == 2))
            System.out.println("v1 is 1 AND v2 is 2");
        if((v1 == 1) || (v2 == 1))
            System.out.println("v1 is 1 OR v2 is 1");
    }
}
